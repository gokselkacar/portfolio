def test_train_model_runs():
    """Tests if train_model runs without error and returns a model."""
    feature_names = ['DayIndex', 'MA_short', 'MA_long', 'Volatility', 'Return', 'Diff_MA']
    X_train = pd.DataFrame(np.random.rand(10, len(feature_names)), columns=feature_names)
