def test_add_features_nan_handling():
    """Tests if rows with NaNs generated by rolling windows are dropped."""
    # N=6, short=2, long=3, vol=2. max_win=3. Dropna should remove rows
    # until all essential features are non-NaN. MA_long needs 3 pts. Volatility needs 2.
    # So, valid data should start from index 2 (3rd row).
    closes = [100.0, 101.0, 102.0, 100.0, 103.0, 104.0]
    dates = pd.date_range(start='2023-01-01', periods=len(closes), freq='D')
    data = pd.DataFrame({'Date': dates, 'Close': closes})
    data_with_features_processed = add_features(data)

    assert data_with_features_processed is not None
    # Expected length after dropna should be original_len - (max_window - 1)
    # If max_window requires W points, first W-1 points might have NaNs.
    # Here max_window seems related to long_window = 3. Expect len = 6 - (3-1?) = 4?
    # Let's check no NaNs remain instead of exact length.
    essential_model_cols = ['DayIndex', 'MA_short', 'MA_long', 'Volatility', 'Return', 'Diff_MA']
    assert data_with_features_processed[essential_model_cols].isnull().sum().sum() == 0


