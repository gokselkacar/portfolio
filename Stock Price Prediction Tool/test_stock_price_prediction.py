def test_add_features_columns():
    """Tests if add_features creates the expected columns."""
    # Create minimal valid input DataFrame
    data = pd.DataFrame({
        'Date': pd.to_datetime(['2023-01-01', '2023-01-02', '2023-01-03', '2023-01-04', '2023-01-05']),
        'Close': [100.0, 101.0, 102.0, 100.0, 103.0]
    })
    data_with_features = add_features(data)
    assert data_with_features is not None
    # Ensure Date column is preserved if present initially (though not used as feature)
    # And check essential feature/intermediate columns are added
    expected_cols = ['Date', 'Close', 'DayIndex', 'MA_short', 'MA_long', 'Volatility', 'Return', 'Diff_MA']
    for col in expected_cols:
        assert col in data_with_features.columns

def test_add_features_return_calc():
    """Tests the 'Return' calculation."""
    data = pd.DataFrame({'Date': pd.to_datetime(['2023-01-01', '2023-01-02']), 'Close': [100.0, 101.0]})
    # Need enough data for windows used by add_features logic for this length (N=2)
    # short=max(2,0)=2, long=max(3,0)=3, vol=max(2,0)=2
    # Since N < min_required (3), add_features should return None or handle gracefully
    # Let's test with slightly more data where N >= min_required
    data = pd.DataFrame({'Date': pd.to_datetime(['2023-01-01', '2023-01-02', '2023-01-03']), 'Close': [100.0, 101.0, 100.5]})
    # N=3, short=2, long=3, vol=2. min_required=3. Should run.
    data_with_features = add_features(data)
    assert data_with_features is not None
    # Data after dropna might be empty or shorter. Check based on actual logic if needed.
    # Let's focus on the calculation itself assuming output exists
    # Need to calculate Return *before* dropna in add_features internally
    temp_data = data.copy()
    temp_data['Return'] = temp_data['Close'].pct_change().fillna(0)

    assert temp_data['Return'].iloc[0] == 0.0
    assert abs(temp_data['Return'].iloc[1] - 0.01) < 1e-9 # (101-100)/100
    assert abs(temp_data['Return'].iloc[2] - (100.5 - 101.0)/101.0) < 1e-9 # (100.5-101)/101


